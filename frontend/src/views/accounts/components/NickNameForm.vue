								class="btn"
								id="nick-name-check-btn"
								type="submit"
								@click.prevent="doubleCheck"
							>
								CHECK
							</button>
						</div>
					</form>
				</div>
			</div>
		</div>
	</div>
</template>

<script>
import { useStore } from 'vuex'
import { reactive } from 'vue'

export default {
	name: 'NickNameForm',
	setup() {
